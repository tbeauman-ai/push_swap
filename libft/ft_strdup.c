#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ret;
	int		i;

	ret = (char *)malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (!ret)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		ret[i] = s1[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
